#A program to implent list using Stack#
#!/user/bin/python
class myStack:
     def __init__(self):
         self.container = []
     def isEmpty(self):
         return self.size() == 0
     def push(self, item):
         self.container.append(item)
     def pop(self):
         return self.container.pop(0)
     def size(self):
         return len(self.container)
s = myStack()
s.push('1')
s.push('2')
print(s.pop())
print()

